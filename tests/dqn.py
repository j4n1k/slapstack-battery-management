def get_env(sim_parameters: SimulationParameters,
            log_frequency: int,
            nr_zones: int, logfile_name: str, log_dir: str,
            partitions=None, reward_setting=1, state_converter=True):
    if partitions is None:
        partitions = [None]
    seeds = [56513]
    if state_converter:
        action_converters = [BatchFIFO(),
                             ClosestOpenLocation(very_greedy=False),
                             FixedChargePolicy(100)
                             ]
        feature_list = ["agv_id", "n_depleted_agvs", "n_free_agv", "n_working_agvs", "avg_battery_working",
                 "battery_cs1", "battery_cs2", "utilization", "queue_len_cs1", "queue_len_cs2",
                 "global_fill_level", "curr_agv_battery", "dist_to_cs",
                 "queue_len_retrieval_orders", "queue_len_delivery_orders",
                 "hour_sin", "hour_cos", "day_of_week", "free_cs_available", "avg_entropy"]
        decision_mode = "charging_check"
        return SlapEnv(
            sim_parameters, seeds, partitions,
            logger=ExperimentLogger(
                filepath=log_dir,
                n_steps_between_saves=log_frequency,
                nr_zones=nr_zones,
                logfile_name=logfile_name),
            state_converter=FeatureConverterCharging(
                feature_list,
                reward_setting=reward_setting, decision_mode=decision_mode),
             action_converters=action_converters
        )
    else:
        return SlapEnv(
            sim_parameters, seeds, partitions,
            logger=ExperimentLogger(
                filepath=log_dir,
                n_steps_between_saves=log_frequency,
                nr_zones=nr_zones,
                logfile_name=logfile_name),
            action_converters=[BatchFIFO(),
                               ClosestOpenLocation(very_greedy=False),
                               LowTHChargePolicy(20)])


